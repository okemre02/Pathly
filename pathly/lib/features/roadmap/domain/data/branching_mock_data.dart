  /// Get career path by ID
  static CareerPath? getCareerPath(String id) {
    try {
      return careerPaths.firstWhere((p) => p.id == id);
    } catch (_) {
      return null;
    }
  }

  /// Get sub-path by ID
  static SubPath? getSubPath(String id) {
    return subPaths[id];
  }

  /// Get all sub-paths for a career path
  static List<SubPath> getSubPathsForCareer(String careerPathId) {
    final career = getCareerPath(careerPathId);
    if (career == null) return [];

    return career.subPathIds
        .map((id) => subPaths[id])
        .whereType<SubPath>()
        .toList();
  }
